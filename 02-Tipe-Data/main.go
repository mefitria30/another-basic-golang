// tiga tipe data dasar di golang adalah number (integer (unsigned integer atau angka yang tidak boleh negatif, signed integer), float (bilangan desimal dengan koma)), string, boolean

package main

import "fmt"

func main() {
	fmt.Println(1)             // number integer
	fmt.Println(1.1)           // number float
	fmt.Println("Hi, fitria")  // string
	fmt.Println(true && false) // boolean
}
